                    <p >The  7/14 days induction program is basically designed to make a new employee aware of the Organizations Objectives, structure and procedures of various  functions of the hotel.  The purpose  of the program is to provide insight into the functioning of various departments and familiarize a new entrant with the hotel and its people.</p>
