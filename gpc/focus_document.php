                    <h2><span style="font-family: Arial" class="headingContent">Front Office </h2>
                    <p>Front Office is the first contact point for the  guest and it is where the first impression is formed. This is the department where guest reservations are processed, check-ins and check-outs take  place. This department also follows-up on the comfort levels of guests and receives feedback. </p>
                    <p>The various sections of Front Office are as follows: </p>
                    <p>1.Telephones </p>
                    <p>2.Reservations </p>
                    <p>3.Reception </p>
                    <p>4. Bell Desk </p>
                    <p>5.Cashier </p>
                    <p>6.Lobby Desk </p>
                    <p>7. Business  Center </p>
                    <p style="line-height: 150%"><strong>Telephones: </strong></p>
                    <p>The telephones department receives the incoming  calls from outside the hotel or from within the hotel and connects the call to the required extension as per the request. </p>
                    <p>The inductee has to focus on: </p>
                    <ul style="margin-top: 0in">
                      <li>The standard phrases/language used, speed of action and the process. </li>
                      <li>Giving all relevant information as per the guest request. </li>
                      <li>Recording the relevant data pertaining to the usage of telephones from respective     rooms/extensions. </li>
                    </ul>
                    <p style="line-height: 150%"><strong>Room Reservations: </strong></p>
                    <p>The reservations department receives the requests  for reservation of rooms or cancellations through telephone, fax, letter or e-mail and makes the reservation with the assistance of computer or  politely refuse in case of non-availability of rooms. They also deal with any other request like amendment/cancellation accordingly. </p>
                    <p style="line-height: 200%">The Inductee has to focus on: </p>
                    <ul style=" line-height:25px;">
                      <li>How the call is received, responded and processed. </li>
                      <li>How the bookings are taken & recorded. </li>
                      <li>Types of rooms and Tariff Details. </li>
                      <li>Discount & credit policy. </li>
                      <li>Passing all relevant information regarding room reservation requests to reception. </li>
                    </ul>
