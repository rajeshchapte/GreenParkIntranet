                    <p >You would learn what is expected of you while you are working in the Hotel. Probably, this has set you wondering if you possess the requisite qualities to succeed in the hotel industry. Well, here is a checklist of personal qualities to help you assess for yourself how well you are suited to working in a hotel. </p>
                    <p >1. SELF CONTROL: </p>
                    <p >In a hotel, the first thing you must learn is that the Customer is always right. Hence you'll be expected to be polite, calm, pleasant and friendly. Arguments, discussions, excuses and explanations are to be avoided at any cost because they only add fuel to the fire instead of helping you to resolve the problem. </p>
                    <p >2. FRIENDLY AND OUTGOING PERSONALITY: </p>
                    <p >If you are naturally friendly and outgoing you are more likely to be successful working in a hotel. Being able to deal with all kinds of guests and visitors combined with a keenness to serve them with a genuine smile is an asset. </p>
                    <p >3. RIGHT ATTITUDE TO WORK: </p>
                    <p >While working in the hotel, you may be called upon to do the most menial jobs, like washing utensils, cleaning toilets, chopping vegetables, etc. You should be prepared to do any kind of job if the circumstances demand it. Dignity of labour is one of the first lessons you’ll have to learn. It is possible; you'll discover along how much more satisfying it is to work with one's own hands. </p>
                    <p >4. GOOD HEALTH AND PHYSICAL FITNESS: </p>
                    <p >You need good mental and physical stamina to survive the long and odd working hours. Moreover, you may have to spend most of your working day on your feet, especially on busy days. Unless you are physically in good health, you won't be able to take the strain. </p>
                    <p >5. WELL GROOMED, PLEASING PERSONALITY: </p>
                    <p >It will give you confidence. if you are well groomed, neat, polite and have an easy way with people. An impressive and pleasing appearance is always a plus point in public relations. </p>
                    <p >6. ABILITY TO FACE CHALLENGES: </p>
                    <p >There is never a dull moment in a hotel. You'll find that each working day throws up new challenges, stimulates you mentally and physically so much that on the rare day when business is dull, you may feel lethargic and bored because you have got accustomed to working at a high-tension level. </p>
                    <p >7. TEAM SPIRIT: </p>
                    <p >In a service industry like a hotel where you work as part of a team, it is very important to be co-operative, helpful and willing to listen to others. You must be flexible and accommodating and enjoy working as part of a team. So, if you are a loner, then this is not exactly your scene. </p>
                    <p >8. SELF DISCIPLINE: </p>
